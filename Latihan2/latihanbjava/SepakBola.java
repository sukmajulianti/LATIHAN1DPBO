class SepakBola{
	
	private String nama;
	private String negara;
	private String tahun;
	private String pemain;
	private String pendiri;
	private String manajer;
	
	SepakBola(){
	}
	
	public void setNama(String nama){
		this.nama = nama;
	}
	
	public String getNama(){
		return this.nama;
	}
	
	public void setNegara(String negara){
		this.negara = negara;
	}
	
	public String getNegara(){
		return this.negara;
	}
	
	public void setTahun(String tahun){
		this.tahun = tahun;
	}
	
	public String getTahun(){
		return this.tahun;
	}
	
	public void setPemain(String pemain){
		this.pemain = pemain;
	}
	
	public String getPemain(){
		return this.pemain;
	}
	
	public void setPendiri(String pendiri){
		this.pendiri = pendiri;
	}
	
	public String getPendiri(){
		return this.pendiri;
	}
	
	public void setManajer(String manajer){
		this.manajer = manajer;
	}
	
	public String getManajer(){
		return this.manajer;
	}
	
	
	
}
	